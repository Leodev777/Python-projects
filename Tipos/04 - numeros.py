# Declaración de variables numéricas
numero = 2           # integer (entero): número sin decimales
decimal = 1.2        # float (número decimal)
imaginario = 2 + 2j  # número complejo (parte real + parte imaginaria)

# Operaciones matemáticas básicas
print(3 + 3)     # Suma: 3 más 3 = 6
print(1 - 3)     # Resta: 1 menos 3 = -2
print(1 * 3)     # Multiplicación: 1 por 3 = 3
print(1 / 3)     # División: 1 dividido 3 = 0.333... (resultado decimal)
print(1 // 3)    # División entera: muestra sólo la parte entera del cociente (0)
print(5 % 3)     # Módulo: devuelve el resto de dividir 5 entre 3 (resto = 2)
print(2 ** 3)    # Potenciación: 2 elevado a la 3 (2*2*2 = 8)

# Actualización de la variable 'numero'
numero = numero + 1  # Primero, se incrementa en 1 (numero pasa de 2 a 3)
numero += 2          # Luego, se suma 2 (numero pasa de 3 a 5)

# Mostrar el valor final de 'numero'
print("numero", numero)  # Imprime: numero 5


